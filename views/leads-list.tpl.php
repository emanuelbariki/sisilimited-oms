    function generateorder(obj) {
        var leadid = $(obj).attr("data-leadid");
        var detailsid = $(obj).attr("data-detailsid");
        
        // console.log(leadid);
        // similar behavior as clicking on a link
        window.location.href = "?module=orders&action=add&leadid="+leadid+"&detailsid="+detailsid+"";
    }

    function closeorder(obj) {
        var leadid = $(obj).attr("data-leadid");
        var detailsid = $(obj).attr("data-detailsid");
        console.log(detailsid);
        console.log(leadid);
        
        window.location.href = "?module=leads&action=marklost&leadid="+leadid+"&id="+detailsid+"";
    }



    function numberWithCommas(number) {
        var parts = number.toString().split(".");
        parts[0] = parts[0].replace(/\B(?=(\d{3})+(?!\d))/g, ",");
        return parts.join(".");
    }

    $(document).ready(function() {
        $(".numberWithCommas").each(function() {
            var num = $(this).text();
            var commaNum = numberWithCommas(num);
            $(this).text(commaNum);
        });
    });
</script>
